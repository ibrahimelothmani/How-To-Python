# Control structures in Python guide the flow of execution of a
# program. They allow the program to respond differently to different
# inputs or situations. In this chapter, we will explore conditional
# statements and various types of loops, including how these can be
# nested to perform complex tasks.

score = 75
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: D or lower")

