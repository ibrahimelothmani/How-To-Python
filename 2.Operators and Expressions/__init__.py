# Addition
addition = 5 + 3
print("Addition:", addition)  # Output: Addition: 8
# Subtraction
subtraction = 5 - 3
print("Subtraction:", subtraction)  # Output: Subtraction: 2
# Multiplication
multiplication = 5 * 3
print("Multiplication:", multiplication)  # Output: Multiplication: 15
# Division (float)
division = 5 / 3
print("Division:", division)  # Output: Division: 1.6666666666666667
# Floor Division (integer)
floor_division = 5 // 3
print("Floor Division:", floor_division)  # Output: Floor
# Modulus (remainder)
modulus = 5 % 3
print("Modulus:", modulus)  # Output: Modulus: 2
# Exponentiation (power)
exponentiation = 5 ** 3
print("Exponentiation:", exponentiation)  # Output: Exponentiation: 125
